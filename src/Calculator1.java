public class Calculator1 {
    static int a = 40;
    static int b = 10;
    static int total = (a+b);
    static int minus = (a-b);
    static int multiplication = (a*b);
    static int division = (a/b);

    public static void main(String[] args) {


      System.out.println("Addition of "+ a +" & "+ b +" = "+total);
      System.out.println("Subtraction of " + a + " - "+ b +" = "+minus );
      System.out.println("Multiplication of "+ a + " & "+ b + " = "+multiplication);
      System.out.println("Division of "+ a + " / "+ b + " = "+division);


    }

}
